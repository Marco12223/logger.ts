
/*
*
* Here we define the levels of the logger in an enum.
* The levels are error, warn, info, and debug.
* @returns {string} error - The error level of the logger.
* @returns {string} warn - The warn level of the logger.
* @returns {string} info - The info level of the logger.
* @returns {string} debug - The debug level of the logger.
*
* */
export enum Levels {
    error = 'error',
    warn = 'warn',
    info = 'info',
    debug = 'debug',
}